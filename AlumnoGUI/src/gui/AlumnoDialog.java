    public void deshabilitarCampos(Boolean deshabilitados) {
        jTextFieldDni.setEditable(deshabilitados);
        jTextFieldNombre.setEditable(deshabilitados);
        jTextFieldApellido.setEditable(deshabilitados);
        jComboBoxSexo.setEnabled(deshabilitados);
        jDateChooserFechaNac.setEnabled(deshabilitados);
        jDateChooserFechaIngreso.setEnabled(deshabilitados);
        jTextFieldCantMatAprob.setEditable(deshabilitados);
        jTextFieldPromedio.setEditable(deshabilitados);
        jComboBoxEstado.setEnabled(deshabilitados);
        
        jButtonGrabar.setVisible(deshabilitados);
    }
    
