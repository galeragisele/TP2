    private void dialog2Alu() throws PersonaException, MiCalendarioException{
        alumno = new Alumno ();
        
        alumno.setDni((jTextFieldDni.getText().isEmpty())? 0:Long.valueOf(jTextFieldDni.getText()));
        alumno.setNombre(String.valueOf(jTextFieldNombre.getText()));
        alumno.setApellido(String.valueOf(jTextFieldApellido.getText()));
        alumno.setFechaNac(new MiCalendario(jDateChooserFechaNac.getCalendar()));
        alumno.setFechaIngreso(new MiCalendario(jDateChooserFechaIngreso.getCalendar()));
        alumno.setCantMatAprob(Integer.valueOf(jTextFieldCantMatAprob.getText()));
        alumno.setPromedio(Double.valueOf(jTextFieldPromedio.getText()));
        String selectedEstado=String.valueOf(jComboBoxEstado.getSelectedItem());
        alumno.setActivo(selectedEstado=="A"?true:false);
        String selected=String.valueOf(jComboBoxSexo.getSelectedItem());
        alumno.setSexo(selected.charAt(0));
    }
    
